                shift
                ;;
            --verbose|-v)
                VERBOSE=true
                shift
                ;;
            --help|-h)
                show_help
                exit 0
                ;;
            --force|-f)
                FORCE_OVERWRITE=true
                shift
                ;;
            --no-backup)
                BACKUP_ENABLED=false
                shift
                ;;
            *)
                log_error "Unknown option: $1"
                log_info "Use --help for usage information"
